export function getAllInspirations() {
  return INSPIRATIONS
}

export function getInspirationsByCategory(category) {
  if (category === 'all') return INSPIRATIONS
  return INSPIRATIONS.filter(item => item.category === category)
}

export function searchInspirations(query) {
  if (!query || !query.trim()) return INSPIRATIONS
  const q = query.trim().toLowerCase()
  return INSPIRATIONS.filter(
    item =>
      item.title.toLowerCase().includes(q) ||
      item.summary.toLowerCase().includes(q) ||
      item.tags.some(tag => tag.toLowerCase().includes(q)),
  )
}

export function getInspirationCountByCategory() {
  const counts = { all: INSPIRATIONS.length }
  for (const cat of CATEGORIES) {
    if (cat.key === 'all') continue
    counts[cat.key] = INSPIRATIONS.filter(item => item.category === cat.key).length
  }
  return counts
}